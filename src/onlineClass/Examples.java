package onlineClass;

public class Examples {
    public static void main(String[] args) {
        // for loop
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
        // while loop
        int number = 4;
        while (number >= 0) {
            System.out.println(number);
            number --;
        }
    }
}